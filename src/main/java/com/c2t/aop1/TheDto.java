package com.c2t.aop1;

public class TheDto {

	private String name;
	private Integer age;

	public TheDto() {
		name = "naresh";
		age = 26;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

}
